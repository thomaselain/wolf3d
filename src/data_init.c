void	load_map(t_env *e)
{
	int		i;

	i = -1;
	e->map = (char**)ft_memalloc(sizeof(char*) * 16);
	while (++i < 16)
		e->map[i] = (char*)ft_memalloc(sizeof(char) * 23);
	e->map_size_x = 23;
	e->map_size_y = 16;
}
